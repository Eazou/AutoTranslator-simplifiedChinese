            int index, l = str.Length;
            for (var i = 0; i < str.Length; i++)
            {
                cha = str.CharAt(i);
                if ((int)str[i] > 1000)
                {
                    index = chtList.IndexOf(cha);
                    if (index != -1) { ret += chsList.CharAt(index); }
                    else
                    {
                        switch (str.CharAt(i))
                        {
                            case "乾":
                                cha = "乾";
                                if (i + 1 < l) { if (str.CharAt(i + 1) == "乾" && str.CharAt(i + 2) == "淨") { cha = "干"; } }
                                var j = i + 1;
