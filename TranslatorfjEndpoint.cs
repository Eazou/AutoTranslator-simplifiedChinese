                                }
                                j = i - 1;
                                if (i > 0) { if (str.CharAt(j) == "餅" || str.CharAt(j) == "腐") { cha = "干"; } }
                                break;
                            case "は":
                                cha = "无";
                                j = i + 1;
                                if (j < l) { if (str.CharAt(j) != "套") { cha = "は"; } }
                                break;
                            case "著":
                                cha = "着";
                                j = i + 1;
                                if (j < l)
                                {
                                    if (str.CharAt(j) == "乎" || str.CharAt(j) == "作" || str.CharAt(j) == "名" || (rg.IsMatch(str.CharAt(j)) == false)) { cha = "著"; }
                                }
                                j = i - 1;
                                if (i > 0)
                                {
                                    if (str.CharAt(j) == "於" || str.CharAt(j) == "名" || str.CharAt(j) == "原" || str.CharAt(j) == "专" || (rg.IsMatch(str.CharAt(j)) == false)) { cha = "著"; }
                                }
                                break;
                            default:
                                break;
                        }
                        ret += cha;
                    }
                }
                else ret += str[i];
            }
            return ret;
        }

